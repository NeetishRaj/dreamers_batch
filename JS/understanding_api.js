function add(num1, num2) {
    return num1 + num2;
}

function sub(num1, num2) {
    return num1 - num2;
}


// Arithmatic API

const Arithmatic = {
    add: function (num1, num2) {
        return num1 + num2;
    },

    sub: function (num1, num2) {
        return num1 - num2;
    }
}


console.log(Arithmatic.add(2, 5)); //7 
console.log(Arithmatic.sub(10, 5)); //5 



