/**
 * 
 * Math class provides with a lot of helpful mathematical functions
 */



console.log(Math.PI); // 3.141.....


// rounding decimal numbers in JS

console.log(Math.ceil(3.6)); //4
console.log(Math.floor(3.6)); //3
console.log(Math.round(3.6)); //4


// Find the max numbers among some numbers

console.log(Math.max(20, -10, 2.5, 60)); //60
console.log(Math.min(20, -10, 2.5, 60)); //-10


// random() returns a random decimal value between 0 and 1

console.log(Math.random()); //0.43556565



// abs() absolute function ignore the sign and just returns the number.
console.log(Math.abs(-9));  // 9
console.log(Math.abs(9));  // 9
 

//Pow() power function helps us do 2 ^ 3 = 8
console.log(Math.pow(2, 3));  //8



//sqrt() returns the square root of a number

console.log(Math.sqrt(4));  //2
console.log(Math.sqrt(8));  //2.828..